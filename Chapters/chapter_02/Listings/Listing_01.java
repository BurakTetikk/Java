package Chapters.chapter_02.Listings;

public class Listing_01 {
    public static void main(String[] args) {
        //Declare radius
        double radius;

        //Declare area
        double area;

        //Assign a radius
        radius = 20;

        //Compute area
        area = radius * radius * 3.14159;

        //Display results
        System.out.println("The area for the circle of radius " + radius + " is " + area);
    }
}
